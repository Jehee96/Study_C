#include <stdio.h>

int main(void)
{
    int x = 10;
    int y = 20;
    printf("x = %d이다.\n", x);
    printf("y = %d이다.\n", y);
    printf("x + y = %d이다.\n", x + y);
    printf("x - y = %d이다.\n", x - y);
    printf("x * y = %d이다.\n", x * y);
    printf("x / y = %d이다.\n", x / y);
    
    return 0;
}